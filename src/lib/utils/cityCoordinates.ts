export function getCityCoordinates(city: string): Coords | null {
  if (!city) return null
  const exact = CITY_COORDINATES[city]
  if (exact) return exact
  const lower = city.toLowerCase()
  if (CITY_COORDINATES[lower]) return CITY_COORDINATES[lower]
  const normalized = lower.replace(/\s+/g, '')
  for (const [key, val] of Object.entries(CITY_COORDINATES)) {
    if (key.replace(/\s+/g, '') === normalized) return val
  }
  return null
}
